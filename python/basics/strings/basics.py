""""String can be either be written in single and double quotes"""

str1 = 'hello'
str2 = "to"
str3 = """user""" #they are multi-line string, can also be written in next line too
print("<--------------------------------------TYPES OF QUOTES---------------------------------------------->")
print(str1 + str2 + str3)


print("<-----------------------------------CHECK STRING USING TYPE()------------------------------------------------->")
print(type(str1))


print("<-------------------------------------ACCESSING STRING USING SLICING AND INDEXING----------------------------------------------->")
print("INDEXING")
print(str1[0])
print(str1[1])
print(str1[2])
print(str1[-1]) #-1 always takes the last value from string and 0 always takes the first value from string

print("SLICING")

print(str1[1:])
print(str1[:1])
print(str1[1:4])
print(str1[::1])
print(str1[1::])




































